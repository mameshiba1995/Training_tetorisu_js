function drawField(){
    for(let i = 0; i < FIELD_HEIGHT; i++){
        for(let j = 0; j < FIELD_WIDTH; j++){
            if(field[i][j] == 0) continue;
            context.fillStyle = "rgba(150, 150, 150, 1.0)";
            context.fillRect(FIELD_X + j * 25, FIELD_Y + i * 25, 25, 25);
        }
    }
}

